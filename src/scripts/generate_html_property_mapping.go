var prefix = `// This variable is auto-generated by generate_html_property_mapping.go
const _mappedProperties = {
`

var postfix = `
};`

func makePropertySetter(propertyName string) string {
	return fmt.Sprintf("  %s: (element, value) => element.%s = value,",
		strings.ToLower(propertyName),
		propertyName)
}

func buildFileContents() string {
	properties := make([]string, len(knownProperties))
	for i, v := range knownProperties {
		properties[i] = makePropertySetter(v)
	}
	return prefix + strings.Join(properties, "\n") + postfix
}

func GenerateHtmlPropertyMapping() {
	propertyMapperPath := path.Join("src", "app", "website", "templates", "html-property-mapper.js")
	err := os.WriteFile(propertyMapperPath, []byte(buildFileContents()), 0o666)
	Fatal(err)
	log.Println("Generated html property mapping file")
}
